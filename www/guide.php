                        <li><strong>Login</strong> - This is your MT4 account number</li>
                        <li><strong>Password</strong> - This is your Trader’s Password</li>
                        <li><strong>Suffix</strong> -  Most of the time you leave this blank. But please check with your broker or your MT4.<br/> Refer to this image below.</li>
                        <li><strong>Account mode</strong> - Select slave.</li>
                    </ul>

                    Then click save.
                    <div class="margin20"></div>
                    <img src="/images/guide_suffix.png"/>
                    <img src="/images/guide_06.png">
                </div>


                <div class="faq-item">
                    <span class="number-mark">7</span> Your account status should show "Connected" in 5-10 minutes. Once it shows connected, you are now done connecting your account to 4X Solutions. The next step now is to connect to a master account (signal provider account).
                    <div class="margin20"></div>
                    <img src="/images/guide_07.png"/>
                </div>



                <div class="faq-item">
                    <span class="number-mark">8</span> Now you need to connect to a master account (signal provider account). Click on portfolio on the left menu. Then click on the ‘edit’ thumbnail.
                    <div class="margin20"></div>
                    <img src="/images/guide_08.png"/>
                </div>

                <div class="faq-item">
                    <span class="number-mark">9</span> Enter the following:
                    <div class="margin20"></div>
                    <ul>
                        <li><strong>Signal Service</strong> - Select the signal provider that you want to connect to</li>
                        <li><strong>Account</strong> - Select your account that you connected previously</li>
                        <li><strong>Money management</strong> - Please refer to the signal provider’s money management recommendation. If you are unsure please select mirror master risk.</li>
                        <li><strong>Price deviation</strong> - Please input 8.0</li>
                    </ul>

                    Hit save.
                    <div class="margin20"></div>
                    <img src="/images/guide_09.png"/>
                </div>

                <div class="faq-item">
                    <span class="number-mark">10</span> Wait a few minutes and your account status should change to connected. Your account is now live and ready to copy trades from the signal provider account.
