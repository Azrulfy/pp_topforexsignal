                <div class="faq-item">
                    <span class="number-mark">3</span> A pop out will appear for you to enter your information. Now connect your broker account to 4X Solutions.
                    <div class="margin20"></div>
                    <img src="/images/guide_03.png"/>
                </div>


                <div class="faq-item">
                    <span class="number-mark">4</span> Select your broker.
                    <div class="margin20"></div>
                    <img src="/images/guide_04.png"/>
                </div>


                <div class="faq-item">
                    <span class="number-mark">5</span> Select your broker server. You can get this information from your broker. This information should also be in your setup email from your broker.
                    <div class="margin20"></div>
                    <img src="/images/guide_05.png"/>
                </div>


                <div class="faq-item">
                    <span class="number-mark">6</span> Enter the rest of the information.
                    <div class="margin10"></div>
